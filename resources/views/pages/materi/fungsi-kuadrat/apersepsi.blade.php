    tertinggi variabelnya adalah 2. Fungsi kuadrat adalah sebuah fungsi
    matematika, yang dinyatakan dalam bentuk persamaan, yaitu persamaan yang
    memiliki bentuk umum 𝑓(𝑥) = 𝑎𝑥2 + 𝑏𝑥 + 𝑐 dimana a, b, dan c adalah
    konstanta-konstanta tertentu dan x adalah variable independent. Gambar
    berikut ini menunjukkan grafik fungsi kuadrat. Posisi bola merupakan posisi
    titik dalam sistem koordinat.
